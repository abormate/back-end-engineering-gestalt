"""
In my opinion, Python has the most elegant syntax for iterating directly over the 
items in a list without worrying about index numbers. If you don't need the index 
number you can use the following syntax:

"""

trees = ['oak', 'pine', 'maple']
for tree in trees:
    print(tree)
# Prints:
# oak
# pine
# maple
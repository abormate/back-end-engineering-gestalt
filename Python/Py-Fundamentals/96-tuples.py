"""
Tuples are collections of data that are ordered and unchangeable. You can think 
of a tuple as a List with a fixed size. Tuples are created with round brackets:

"""

my_tuple = ("this is a tuple", 45, True)
print(my_tuple[0])
# this is a tuple
print(my_tuple[1])
# 45
print(my_tuple[2])
# True
